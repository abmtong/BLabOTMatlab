function GheCalibrate_PlotFits(Result,i,FileName)
%
% USE: GheCalibrate_PlotFits(Result,i,FileName)
%
% Gheorghe Chistol, 9 Feb 2012

%% Create figure and Axes for each MainPlot and Residual Plot
FigH = figure('Color',[0.8203125 0.703125 0.546875],...
              'Units','normalized', 'Position', [0.01+(i-1)*0.02 0.0625 0.6515 0.8359]);

AxesAX = axes('Parent',FigH,'YScale','log','YMinorTick','on','XTickLabel','',...
              'XScale','log','XMinorTick','on','Box','on','NextPlot','add',...
              'Position',[0.0762851509707941 0.670212765957447 0.413602489478644 0.314185617851335]);
              ylabel('Power (NV*NV/Hz)','FontWeight','bold');
AxesAXres = axes('Parent',FigH,'XTickLabel','','XScale','log','XMinorTick','on','NextPlot','add','Box','on',...
                 'Position',[0.0762851509707941 0.550151975683891 0.413602489478644 0.113981762917933]);
                 ylabel('Residual','FontWeight','bold');

AxesAY = axes('Parent',FigH,'YScale','log','YMinorTick','on','XTickLabel','',...
              'XScale','log','XMinorTick','on','Box','on','NextPlot','add',...
              'Position',[0.0762851509707942 0.212268840723802 0.413602489478644 0.314185617851335]);
              ylabel('Power (NV*NV/Hz)','FontWeight','bold');          
AxesAYres = axes('Parent',FigH,'XScale','log','XMinorTick','on','NextPlot','add','Box','on',...
                 'Position',[0.0762851509707942 0.0922080504502456 0.413602489478644 0.113981762917933]);
                 ylabel('Residual','FontWeight','bold');
                 xlabel('Frequency (Hz)','FontWeight','bold');

AxesBX = axes('Parent',FigH,'YScale','log','YMinorTick','on','XTickLabel','','NextPlot','add',...
              'XScale','log','XMinorTick','on','Box','on',...
              'Position',[0.57403795995957 0.670212765957447 0.413602489478644 0.314185617851335]);
              ylabel('Power (NV*NV/Hz)','FontWeight','bold');
AxesBXres = axes('Parent',FigH,'XTickLabel','','XScale','log','XMinorTick','on','NextPlot','add','Box','on',...
                 'Position',[0.57403795995957 0.550151975683891 0.413602489478644 0.113981762917933]);
                 ylabel('Residual','FontWeight','bold');

AxesBY = axes('Parent',FigH,'YScale','log','YMinorTick','on','XTickLabel','','NextPlot','add',...
              'XScale','log','XMinorTick','on','Box','on',...
              'Position',[0.57403795995957 0.212268840723802 0.413602489478644 0.314185617851335]);
              ylabel('Power (NV*NV/Hz)','FontWeight','bold');          
AxesBYres = axes('Parent',FigH,'XScale','log','XMinorTick','on','NextPlot','add','Box','on',...
                 'Position',[0.57403795995957 0.0922080504502456 0.413602489478644 0.113981762917933]);
                 ylabel('Residual','FontWeight','bold');
                 xlabel('Frequency (Hz)','FontWeight','bold');

% Create textbox
annotation(FigH,'textbox',[0.454932584269664 0.0109034267912773 0.151808988764045 0.0420560747663551],...
           'String',FileName,'HorizontalAlignment','center','FontWeight','bold','FitBoxToText','off',...
           'LineStyle','none','BackgroundColor',[0 0 0],'Color',[1 1 1]);

%% Plot the Actual Data
MarkerSize = 15; 
LineWidth  = 1.5;
ColorAX    = [0.1171875 0.5625 1];
ColorAY    = [1 0.64453125 0];
ColorBX    = [0.6015625 0.80078125 0.1953125];
ColorBY    = [0.9296875 0.5078125 0.9296875];
annotation(FigH,'textbox',[0.4393    0.9302    0.0427    0.0421],...
           'String','AX','HorizontalAlignment','center','FontWeight','bold','FitBoxToText','off',...
           'LineStyle','none','BackgroundColor',ColorAX,'Color',[0 0 0]);
annotation(FigH,'textbox',[0.4393    0.4709    0.0427    0.0421],...
           'String','AY','HorizontalAlignment','center','FontWeight','bold','FitBoxToText','off',...
           'LineStyle','none','BackgroundColor',ColorAY,'Color',[0 0 0]);
annotation(FigH,'textbox',[0.9369    0.9302    0.0427    0.0421],...
           'String','BX','HorizontalAlignment','center','FontWeight','bold','FitBoxToText','off',...
           'LineStyle','none','BackgroundColor',ColorBX,'Color',[0 0 0]);
annotation(FigH,'textbox',[0.9369    0.4709    0.0427    0.0421],...
           'String','BY','HorizontalAlignment','center','FontWeight','bold','FitBoxToText','off',...
           'LineStyle','none','BackgroundColor',ColorBY,'Color',[0 0 0]);

BoxAX = annotation(FigH,'textbox',[0.085 0.683+0.01 0.265 0.1],'String','',...
                   'FontWeight','bold','BackgroundColor','none','LineStyle','none');
BoxAY = annotation(FigH,'textbox',[0.085 0.225+0.01 0.265 0.1],'String','',...
                   'FontWeight','bold','BackgroundColor','none','LineStyle','none');
BoxBX = annotation(FigH,'textbox',[0.584 0.683+0.01 0.265 0.1],'String','',...
                   'FontWeight','bold','BackgroundColor','none','LineStyle','none');
BoxBY = annotation(FigH,'textbox',[0.584 0.225+0.01 0.265 0.1],'String','',...
                   'FontWeight','bold','BackgroundColor','none','LineStyle','none');

GheCalibrate_PlotOneChannel(Result.AX,AxesAX,AxesAXres,ColorAX,LineWidth,MarkerSize,BoxAX);
GheCalibrate_PlotOneChannel(Result.AY,AxesAY,AxesAYres,ColorAY,LineWidth,MarkerSize,BoxAY);
GheCalibrate_PlotOneChannel(Result.BX,AxesBX,AxesBXres,ColorBX,LineWidth,MarkerSize,BoxBX);
GheCalibrate_PlotOneChannel(Result.BY,AxesBY,AxesBYres,ColorBY,LineWidth,MarkerSize,BoxBY);
        
end