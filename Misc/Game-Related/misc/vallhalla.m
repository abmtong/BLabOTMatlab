str = '01010100 01001000 01000101 00100000 01010010 01000101 01010100 01010010 01001001 01000010 01010101 01010100 01001001 01001111 01001110 00100000 01001001 01010011 00100000 01000011 01001111';
len = length(str);
num = (len+1)/9;
outstr = zeros(1,num);
for i = 1:num
    stin = (i-1)*9+1;
    enin = i*9-1;
    outstr(i) = bin2dec(str(stin:enin));
end
char(outstr)